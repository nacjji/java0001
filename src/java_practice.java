public class java_practice {
    public static void main(String[] args) {
        // 숫자 오른쪽 정렬, 빈공간 0 삽입
        System.out.printf("%05d\n", 15);
        System.out.printf("%05d\n", 151);
        System.out.printf("%05d\n", 1523);
        System.out.println();
        System.out.printf("%5d\n", 15);
        System.out.printf("%5d\n", 151);
        System.out.printf("%5d\n", 1523);
        System.out.println();

        // 소수 반올림 후 출력
        System.out.printf("%f\n",1.12345);
        System.out.printf("%.2f\n",1.12345);
        System.out.printf("%.3f\n",1.12345);
        System.out.printf("%.4f\n",1.12345);
        System.out.println();

        // bool 값 출력
        System.out.printf("%b\n", 2>1);
    }
}
